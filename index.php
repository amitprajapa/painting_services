<script>
    $(document).ready(function () {
	$('#paintersForm-form').submit(function (event) {
	    var formData = {
		'mobile': $('input[name=paintersFormMobile]').val(),
		'email': $('input[name=paintersFormEmail]').val(),
		'location': $('input[name=paintersFormLocation]').val(),
		'city': $('input[name=paintersFormCity]').val()
	    };
	    jQuery.ajax({
		type: 'POST',
		url: './php/modal-handle.php',
		data: formData,
		dataType: 'json',
		encode: true,
		async: true
	    })
		    .done(function (data)
		    {
			if (data.success) {
                            $('#paintersForm-form .alert-success').toggleClass("d-none");
                            $("#success").html(data.message);
                            setTimeout(function () {
                                $('#paintersForm-form .alert-success').toggleClass("d-none");
                                location.reload();
                            }, 3000);
                        } else {
                            $('#paintersForm-form .alert-danger').toggleClass("d-none");
                            $("#error").html(data.message);
                            setTimeout(function () {
                                $('#paintersForm-form .alert-danger').toggleClass("d-none");
                            }, 3000);
                            return false;
                        }                        
		    })
		    .fail(function (data) {
			console.log(data);
		    });

	    event.preventDefault();
	});
    });
    $('#bookpaintersForm').on('click', function () {
	$('#paintersForm-modal2').show();
    });

    $('#paintersForm-footer').on('click', function () {
	$('#paintersForm-modal2').hide();
    });
</script>




